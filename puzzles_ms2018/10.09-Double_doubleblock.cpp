//Two doubleblock puzzles are superimposed in the grid.
//For each puzzle blacken some cells and enter the numbers from 1-4 in the remaining cells so that in each row and each column exactly two cells are blacked out and each number occurs exactly once. The numbers on the edge indicate the sum of the numbers that are between the two black cells. The numbers around the upper grid show the hints for both puzzles in increasing order. You have to decide which one belongs to which puzzle. Also in the grid all the cells where the blocks from both puzzles overlap are marked.
//
//
//  Example asignment
//
//Size: 5
//
//Marked cells:
//  [1,4] [2,5]
//
